export function getNewReleases() {
    const currentYear = new Date().getFullYear();
    return booksList
        .filter(book => book.year >= 2015)
        .sort((a, b) => b.year - a.year)
        .slice(0, 10);
}

export function getArabicBooks() {
    return booksList.filter(book => book.language === "Arabic");
}

export function getForeignBooks() {
    return booksList.filter(book => book.language === "English");
}

export function getTrendingBooks() {
    return booksList.filter(book => book.isTrending === true);
}

export function getMostPopular() {
    return booksList
        .sort((a, b) => b.rating - a.rating)
        .slice(0, 10);
}

